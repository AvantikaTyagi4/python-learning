# range function is used to print sequence of numbers
# it starts with 0 and argument is not included e.g., 10 is not included

for i in range(10):
    print(i)

# for printing number from start to end like 5 -9
for i in range(5,10):
    print(i)


# for printing number between range and skips(or step forward) the number e.g., 5,7,9
for i in range(5,10,2):
    print(i)